#include "DS12887.h"

/***************************************************
************DS12887初始化程序************************
****************************************************/
void DS_init()
{
    DS_A=0x20;
	DS_B=0x12;
} 


void write_time() 
{
  	DS_A=0x2f;          //寄存器Ａ
	DS_B|=0x90; 
  	Years=0x17;         //年 
  	Month=0x04;         //月 
  	Day=0x08;           //日 
  	Week=0x06;          //星期 
  	Hour=0x11;          //时 
  	Minute=0x02;        //分 
  	Second=0x00;        //秒 
  	DS_B &=0x7f;        //寄存器Ａ 
}
